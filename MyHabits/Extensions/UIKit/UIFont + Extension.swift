import UIKit

extension UIFont {
    
    //Semibold
    
    static func sfProDisplaySemi24() -> UIFont? {
        return UIFont.init(name: "SF-Pro-Display-Semibold", size: 24)
    }
    
    static func sfProDisplaySemi20() -> UIFont? {
        return UIFont.init(name: "SF-Pro-Display-Semibold", size: 20)
    }
    
    static func sfProTextSemi17() -> UIFont? {
        return UIFont.init(name: "SF-Pro-Text-Semibold", size: 17)
    }
    
    static func sfProTextSemi13() -> UIFont? {
        return UIFont.init(name: "SF-Pro-Text-Semibold", size: 13)
    }
    
    //Regular
    static func sfProTextRegular17() -> UIFont? {
        return UIFont.init(name: "SF-Pro-Text-Regular", size: 17)
    }
    
    static func sfProTextRegular13() -> UIFont? {
        return UIFont.init(name: "SF-Pro-Text-Regular", size: 13)
    }
    
    static func sfProTextRegular12() -> UIFont? {
        return UIFont.init(name: "SF-Pro-Text-Regular", size: 12)
    }
}
